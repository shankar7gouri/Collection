package testJava.com;

public class auto_increment {

	public static void main(String[] args) {
	
		int i=100;
		i++;
		System.out.println(i);
		int j=897;
		j+=5;
		System.out.println(j);
		
		int k=100;
		k--;
		System.out.println(k);
		int l=567;
		l-=56;
		System.out.println(l);
		
		
		
		
		

	}

}
